<template>
    <div>
        CARD
        <div v-if="loading">
            loading...
        </div>
        <div v-else>
            cid: {{ cid }}
        </div>
    </div>

</template>

<script>
export default({
    data() {
        return {
            cid: 0,
            loading: false,
        };
    },
    watch: {
        // '$route'() {
        //     this.cid = this.$route.params.cid;
        //     console.log(this.$route);
        // },

        '$route': {
            handler: 'fetchData',
            immediate: true,
        },
    },
    methods: {
        fetchData() {
            this.loading = true;
            setTimeout(()=> {
                this.cid = this.$route.params.cid;
                this.loading = false;
            }, 500);
        },
    },
});
</script>

